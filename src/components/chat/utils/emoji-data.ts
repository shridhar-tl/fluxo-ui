export function applyShortcodes(text: string, codes: Record<string, string>): string {
    if (!text) return text;
    let out = text;
    const keys = Object.keys(codes).sort((a, b) => b.length - a.length);
    for (const key of keys) {
        const escaped = key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
        out = out.replace(new RegExp(escaped, 'g'), codes[key]);
    }
    return out;
}

export function tryReplaceTrailingShortcode(text: string, codes: Record<string, string>): string | null {
    if (!text) return null;
    const lastChar = text[text.length - 1];
    if (lastChar !== ' ' && lastChar !== '\n' && lastChar !== '\t') return null;
    const trimmed = text.slice(0, -1);
    const tokenStart = Math.max(trimmed.lastIndexOf(' '), trimmed.lastIndexOf('\n'), trimmed.lastIndexOf('\t')) + 1;
    const token = trimmed.slice(tokenStart);
    if (!token) return null;
    const replacement = codes[token];
    if (!replacement) return null;
    return text.slice(0, tokenStart) + replacement + lastChar;
}
